package com.px.mwp.model;

/**
 *
 * @author Oat
 */
public class UserStructureModel {

    private int id;
    private String name;
    private int type; //[0=User, 1=Structure]

    public UserStructureModel() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

}
